package com.devefx.crawler.model;

public class SchoolMagic {

    private int skillId;
    private String name;
    private int skillLevel;
    private int roleLevel;
    private int requireType;
    private String description;
    private String costDescription;
    private int hpCost;
    private int mpCost;
    private int spCost;
    private int costType;
    
    public int getSkillId() {
        return skillId;
    }
    public void setSkillId(int skillId) {
        this.skillId = skillId;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getSkillLevel() {
        return skillLevel;
    }
    public void setSkillLevel(int skillLevel) {
        this.skillLevel = skillLevel;
    }
    public int getRoleLevel() {
        return roleLevel;
    }
    public void setRoleLevel(int roleLevel) {
        this.roleLevel = roleLevel;
    }
    public int getRequireType() {
        return requireType;
    }
    public void setRequireType(int requireType) {
        this.requireType = requireType;
    }
    public String getDescription() {
        return description;
    }
    public void setDescription(String description) {
        this.description = description;
    }
    public String getCostDescription() {
        return costDescription;
    }
    public void setCostDescription(String costDescription) {
        this.costDescription = costDescription;
    }
    public int getHpCost() {
        return hpCost;
    }
    public void setHpCost(int hpCost) {
        this.hpCost = hpCost;
    }
    public int getMpCost() {
        return mpCost;
    }
    public void setMpCost(int mpCost) {
        this.mpCost = mpCost;
    }
    public int getSpCost() {
        return spCost;
    }
    public void setSpCost(int spCost) {
        this.spCost = spCost;
    }
    public int getCostType() {
        return costType;
    }
    public void setCostType(int costType) {
        this.costType = costType;
    }
}

